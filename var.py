# Инициализируем переменную целочисленным значением
var = 8
print(var)

# Присваиваем переменной значение числа с плавающей точкой
var = 3.142
print(var)

# Присваиваем переменной строковое значение
var = 'Python in easy steps'
print(var)

# Присваиваем переменной логическое значение
var = True
print(var)